package StringFunction;

public class IndexOf {

	public static void main(String[] args) {
		String a="tutorial who the hell are you";
		System.out.println(a.indexOf('u'));
		System.out.println(a.indexOf('w'));
		
		System.out.println(a.indexOf('t',5));
		System.out.println(a.indexOf("hell"));
		
	}

}
